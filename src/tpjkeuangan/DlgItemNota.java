    private void udfDeleteItem() {
        if(JOptionPane.showConfirmDialog(this, "Anda yakin untuk menghapus keterangan nota ini?", "Confirm", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            if(tblItem.getSelectedRow()>=0){
                int iRow[]= tblItem.getSelectedRows();
                int rowPalingAtas=iRow[0];
                for (int a=0; a<iRow.length; a++){
                    try{
                        conn.createStatement().executeUpdate("delete from nota_item_trx where item_trx='"+tblItem.getValueAt(tblItem.getSelectedRow(), 0).toString()+"'");
                    }catch(SQLException se){
                        JOptionPane.showMessageDialog(this, se.getMessage());
                    }
                    ((DefaultTableModel)tblItem.getModel()).removeRow(tblItem.getSelectedRow());

                }

                if(tblItem.getRowCount()>0 && rowPalingAtas<tblItem.getRowCount()){
                    tblItem.setRowSelectionInterval(rowPalingAtas, rowPalingAtas);
                }else{
                    if(tblItem.getRowCount()>0)
                        tblItem.setRowSelectionInterval(rowPalingAtas-1, rowPalingAtas-1);
                    else
                        jTextArea1.requestFocus();
                }
                if(tblItem.getSelectedRow()>=0)
                    tblItem.changeSelection(tblItem.getSelectedRow(), 0, false, false);
            }
        }
    }

    private void udfUpdateItem(){
        int iRow=tblItem.getSelectedRow();
        if(iRow<0) return;
        boolean isNew=true;
        try{
            ResultSet rs=conn.createStatement(ResultSet.TYPE_FORWARD_ONLY, ResultSet.CONCUR_UPDATABLE)
                    .executeQuery("select * from nota_item_trx where item_trx='"+tblItem.getValueAt(iRow, 0).toString()+"'");
            if(rs.next())
                isNew=false;
            else
                rs.moveToInsertRow();

            rs.updateString("item_trx", jTextArea1.getText());

            if(isNew)
                rs.insertRow();
            else
                rs.updateRow();

            ((DefaultTableModel)tblItem.getModel()).setValueAt(jTextArea1.getText(), iRow, 0);
        }catch(SQLException se){

        }
    }

    private void udfDeleteSatuan() {
        if(JOptionPane.showConfirmDialog(this, "Anda yakin untuk menghapus Satuan ini?", "Confirm", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            if(tblSatuan.getSelectedRow()>=0){
                int iRow[]= tblSatuan.getSelectedRows();
                int rowPalingAtas=iRow[0];
                for (int a=0; a<iRow.length; a++){
                    try{
                        conn.createStatement().executeUpdate("delete from nota_satuan where " +
                                "coalesce(satuan,'')='"+tblSatuan.getValueAt(tblSatuan.getSelectedRow(), 0).toString()+"' and " +
                                "coalesce(ket_satuan,'')='"+tblSatuan.getValueAt(tblSatuan.getSelectedRow(), 1).toString()+"'");
                    }catch(SQLException se){
                        JOptionPane.showMessageDialog(this, se.getMessage());
                    }
                    ((DefaultTableModel)tblSatuan.getModel()).removeRow(tblSatuan.getSelectedRow());

                }

                if(tblSatuan.getRowCount()>0 && rowPalingAtas<tblSatuan.getRowCount()){
                    tblSatuan.setRowSelectionInterval(rowPalingAtas, rowPalingAtas);
                }else{
                    if(tblSatuan.getRowCount()>0)
                        tblSatuan.setRowSelectionInterval(rowPalingAtas-1, rowPalingAtas-1);
                    else
                        jTextArea1.requestFocus();
                }
                if(tblSatuan.getSelectedRow()>=0)
                    tblSatuan.changeSelection(tblSatuan.getSelectedRow(), 0, false, false);
            }
        }
    }

    private void udfUpdateSatuan(){
        int iRow=tblSatuan.getSelectedRow();
        if(iRow<0) return;
        boolean isNew=true;
        try{
            ResultSet rs=conn.createStatement(ResultSet.TYPE_FORWARD_ONLY, ResultSet.CONCUR_UPDATABLE)
                    .executeQuery("select * from nota_satuan where " +
                                "coalesce(satuan,'')='"+tblSatuan.getValueAt(tblSatuan.getSelectedRow(), 0).toString()+"' and " +
                                "coalesce(ket_satuan,'')='"+tblSatuan.getValueAt(tblSatuan.getSelectedRow(), 1).toString()+"'");
            if(rs.next())
                isNew=false;
            else
                rs.moveToInsertRow();

            rs.updateString("satuan", txtSatuan.getText());
            rs.updateString("ket_satuan", txtKetSatuan.getText());

            if(isNew)
                rs.insertRow();
            else
                rs.updateRow();

            ((DefaultTableModel)tblSatuan.getModel()).setValueAt(txtSatuan.getText(), iRow, 0);
            ((DefaultTableModel)tblSatuan.getModel()).setValueAt(txtKetSatuan.getText(), iRow, 1);
        }catch(SQLException se){

        }
    }
