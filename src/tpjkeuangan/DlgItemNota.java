    private void udfInitForm(){
        try{
            ResultSet rs=conn.createStatement().executeQuery("select item_trx from nota_item_trx order by 1 ");
            ((javax.swing.table.DefaultTableModel)tblItem.getModel()).setNumRows(0);
            while(rs.next()){
                ((javax.swing.table.DefaultTableModel)tblItem.getModel()).addRow(new Object[]{rs.getString(1)});
            }
            rs.close();
            if(tblItem.getRowCount()>0)
                tblItem.setRowSelectionInterval(0, 0);

            rs=conn.createStatement().executeQuery("select coalesce(satuan,'') as satuan, coalesce(ket_satuan,'') as ket_satuan from nota_satuan order by 1, 2");
            ((javax.swing.table.DefaultTableModel)tblSatuan.getModel()).setNumRows(0);
            while(rs.next()){
                ((javax.swing.table.DefaultTableModel)tblSatuan.getModel()).addRow(new Object[]{
                    rs.getString(1),rs.getString(2)
                });
            }
            rs.close();
            if(tblSatuan.getRowCount()>0)
                tblSatuan.setRowSelectionInterval(0, 0);
            rs.close();

        }catch(SQLException se){
            JOptionPane.showMessageDialog(this, se.getMessage());
        }
    }
