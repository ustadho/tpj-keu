    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int iRow=jXTable1.getSelectedRow();
        if(iRow>=0){
            if(JOptionPane.showConfirmDialog(this, "Anda yakin untuk menghapus satuan '"+jXTable1.getValueAt(iRow, 0).toString()+"' ini?", "Confirm", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
                try{
                    ResultSet rs=conn.createStatement().executeQuery("select * from nota_detail where satuan='"+jXTable1.getValueAt(iRow, 0).toString()+"'");

                    if(rs.next()){
                        JOptionPane.showMessageDialog(this, "Satuan tidak bisa dihapus karena telah digunakan untuk nota");
                        txtUnit.requestFocus();
                        return;
                    }

                    int i=conn.createStatement().executeUpdate("Delete from nota_satuan where satuan='"+jXTable1.getValueAt(iRow, 0).toString()+"'");
                    if(i>0){
                        myModel.removeRow(iRow);
                    }

                }catch(SQLException se){
                    System.err.println(se.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    public class SelectionListener implements ListSelectionListener {
         JTable table;
         int rowPos;
         int colPos;

        // It is necessary to keep the table since it is not possible
        // to determine the table from the event's source
        SelectionListener(JTable table) {
            this.table = table;
        }
        public void valueChanged(ListSelectionEvent e) {
            if(table.getSelectedRow()>=0){
                txtUnit.setText(table.getValueAt(table.getSelectedRow(), 0).toString());
                cmbFormat.setSelectedItem(table.getValueAt(table.getSelectedRow(), 1).toString());
                txtUnit.setEditable(false);
                btnDelete.setEnabled(true);
                btnSave.setText("Update");
            }
        }
    }

