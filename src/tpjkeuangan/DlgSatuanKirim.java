    private void udfLoadGrid(int rowPosition){
        try {
            ResultSet rs = conn.createStatement().executeQuery("select distinct coalesce(satuan,''), " +
                    "coalesce(formats,'') as formats from nota_satuan order by  coalesce(satuan,'')");

            myModel=(DefaultTableModel)jXTable1.getModel();
            myModel.setNumRows(0);
            jXTable1.setModel(myModel);

            while(rs.next()){
                myModel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                });
            }

            if(myModel.getRowCount()>0) jXTable1.setRowSelectionInterval(rowPosition, rowPosition);

        } catch (SQLException ex) {
            Logger.getLogger(DlgSatuanKirim.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
